export function translateRoomName(name: string, lang: Lang): string {
  if (lang === "de") return name;
  return ROOM_NAME_DE_TO_EN[name] ?? name;
}

export function translateText(text: string, lang: Lang): string {
  if (!text || lang === "de") return text;
  let out = text;
  for (const [de, en] of TERMS_DE_TO_EN) {
    // word-boundary match, case-insensitive; preserve sentence flow
    const re = new RegExp(`\\b${de.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\b`, "gi");
    out = out.replace(re, en);
  }
  return out;
}
